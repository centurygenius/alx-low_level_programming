#include <stdio.h>

/**
 * add - sums two integers
 *@a: first num
 *@b: second num
 *Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 *@a: first num
 *@b: second num
 *Return: differece
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 *@a: first num
 *@b: second num
 *Return: multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two integers
 *@a: first num
 *@b: second num
 *Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two integers
 *@a: first num
 *@b: second num
 *Return: modulus
 */

int mod(int a, int b)
{
	return (a % b);
}
